    private void mostrardatos() throws SQLException {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre y apellido");
        modelo.addColumn("Direccion");
        modelo.addColumn("Gmail");
        modelo.addColumn("Telefono");
        modelo.addColumn("Estado civil");
        tableDatos.setModel(modelo);
        String data[] = new String[6];
        
        Connection conexion = (Connection) Conexion.getConnection();
        PreparedStatement ps;
        try{
        ps=(PreparedStatement) conexion.prepareStatement("select * from cliente");
        ResultSet rs; 
        rs = ps.executeQuery();
        while(rs.next()){
            data[0] = rs.getString(1);
            data[1] = rs.getString(2);
            data[2] = rs.getString(3);
            data[3] = rs.getString(4);
            data[4] = rs.getString(5);
            data[5] = rs.getString(6);
            modelo.addRow(data);
        }
       }catch(SQLException e){
            System.out.println("Error al mostrar datos");
       }
  
    }

