    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Connection conexion = (Connection) Conexion.getConnection();
        PreparedStatement ps;
        try{
        ps=(PreparedStatement) conexion.prepareStatement("UPDATE cliente SET nombre_apellido ='"+txtNombre.getText()+"',direccion='"+txtDireccion.getText()+"',gmail='"+txtGmail.getText()+"',telefono='"+txtTelefono.getText()+"',estado_civil='"+comboEstado.getSelectedItem()+"' WHERE id='"+txtId.getText()+"'");
       
        int indice = ps.executeUpdate();
        if(indice > 0) {
            JOptionPane.showMessageDialog(null, "DATOS ACTUALIZADO CORRECTAMENTE");
            mostrardatos();
            limpiarEntrada();
        }else{
            JOptionPane.showMessageDialog(null, "No selecciono Fila");
        } 
        
        limpiarEntrada();
       }catch(SQLException e){
             JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR DATOS"+e);
       }
    }//GEN-LAST:event_btnEliminarActionPerformed

